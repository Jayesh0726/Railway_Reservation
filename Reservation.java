    public void LocalStartplace(){
       
        try{
            
            pst = con.prepareStatement("SELECT start_place FROM train_details");
            rs = pst.executeQuery();
            txtsplace.removeAllItems();
            
            while ( rs.next() )
            {
                txtsplace.addItem( rs.getString(1));
                
            }
        }
        catch ( Exception e){
            e.printStackTrace();
        }
    }
    public void LocalEndplace(){
       
        try{
            
            pst = con.prepareStatement("SELECT destination_place FROM train_details");
            rs = pst.executeQuery();
            txtdeplace.removeAllItems();
            
            while ( rs.next() )
            {
                txtdeplace.addItem( rs.getString(1));
                
            }
        }
        catch ( Exception e){
            e.printStackTrace();
        }
    }
    public void load(){
        String splace = txtsplace.getSelectedItem().toString();
        String eplace = txtdeplace.getSelectedItem().toString();
        
        try{
           pst = con.prepareStatement("SELECT * FROM train_details where start_place = ? and destination_place = ?");
           pst.setString(1, splace);
           pst.setString(2, eplace);
           rs = pst.executeQuery();
           
           if (rs.next()==true){
               txttno.setText(rs.getString(1));
               txttname.setText(rs.getString(2));
               txtprice.setText(rs.getString(5));
           }
           else{
               JOptionPane.showMessageDialog(this, "No Train Available");
           }
        }
        catch (Exception e){
            e.printStackTrace();
        }
    }

