    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String Splace = txtsplace.getSelectedItem().toString();
        String Endplace = txtdeplace.getSelectedItem().toString();
        String trainnum = txttno.getText();
        String trainname = txttname.getText();
        SimpleDateFormat date_form = new SimpleDateFormat("yyyy-MM-dd");
        String date = date_form.format(txtdate.getDate());
        String Price = txtprice.getText();
        String ticketno = txtticketno.getText();
        String Total = txttotal.getText();
        
        
        try {
            pst = con.prepareStatement("insert into reservation(S_place,D_place,T_num,T_name,Date,price_n,ticket_num,total)value(?,?,?,?,?,?,?,?)");
            
            pst.setString(1, Splace);
            pst.setString(2, Endplace);
            pst.setString(3, trainnum);
            pst.setString(4, trainname);
            pst.setString(5, date);
            pst.setString(6, Price);
            pst.setString(7, ticketno);
            pst.setString(8, Total);
            int k = pst.executeUpdate();
            
            if(k==1){
                JOptionPane.showMessageDialog(this, "Reservation Successful");
            }
            else{
                JOptionPane.showMessageDialog(this, "Reservation Unsuccessful");
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(addtrain.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

